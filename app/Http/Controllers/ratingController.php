    {
        //
        $inputs=$request->all();

        $rating = rating::where('user_id','=', Auth::user()->id)->where('unit_id',$inputs['unit_id'])->first();
//        $rating = Auth::user()->UnitRating;
        $inputs['user_id'] = Auth::user()->id;

        if($rating){
            $rated = $rating->update($inputs);

        }else{
            $rated = rating::create($inputs);
        }

        return response()->json(array('success'=>$rating));


